# entrada

num1 = float(input('Digite o primeiro número: '))
num2 = float(input('Digite o segundo número: '))
num3 = float(input('Digite o terceiro número: ')) 

# processamento

soma2Primeiros = num1 + num2
diferenca2Ultimos = num2 - num3

# saida

print(f'A soma dos dois primeiros: {num1} + {num2} = {soma2Primeiros}')
print(f'A diferença entre os dois últimos: {num2} - {num3} = {diferenca2Ultimos}')